# coding: utf-8

n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
ni = n1 // n2
e = n1 ** n2
mod = n1 % n2

print('A soma é {}\nO produto é {}\nA divisão é {:.3f}\nA divisão inteira é {}\nA potência é {}\nO módulo é {}'.format(s, m, d, ni, e, mod))



